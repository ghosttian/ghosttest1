+ (NSString*)getAllLetterFirstPinyin:(NSString *)word
{
    NSMutableString *firstLetter = [[[NSMutableString alloc] init] autorelease];
    for (int i = 0; i < [word length]; i++)
    {
        unichar ch = [word characterAtIndex:i];
        if ( (ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z') ) {
            [firstLetter appendFormat:@"%c", ch];
        } else {
            [firstLetter appendFormat:@"%c", pinyinFirstLetterNoFormat(ch)];
        }
    }
    return firstLetter;
}

